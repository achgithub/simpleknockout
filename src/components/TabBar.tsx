const TABS = [
  { path: '/',        labelKey: 'tabBar.knockouts', Icon: TrophyIcon },
  { path: '/players', labelKey: 'tabBar.players',   Icon: PeopleIcon },
  { path: '/settings',labelKey: 'tabBar.settings',  Icon: GearIcon   },
] as const;

export function TabBar() {
  const navigate = useNavigate();
  const { pathname } = useLocation();
  const { t } = useTranslation();

  return (
    <div className="shrink-0 bg-white border-t border-gray-200 pb-safe">
      <div className="flex">
        {TABS.map(({ path, labelKey, Icon }) => {
          const active = pathname === path;
          return (
            <button
              key={path}
              onClick={() => navigate(path)}
              className={clsx(
                'flex-1 flex flex-col items-center justify-center py-2 gap-0.5 transition-colors',
                active ? 'text-brand-600' : 'text-gray-400',
              )}
            >
              <Icon />
              <span className="text-[10px] font-semibold tracking-wide">{t(labelKey)}</span>
            </button>
          );
        })}
      </div>
    </div>
  );
}
